controller.left.onEvent(ControllerButtonEvent.Pressed, function () {
    if (game_state == 1) {
        if (controller.right.isPressed()) {
            Idle = true
        }
        if (!(controller.right.isPressed())) {
            Idle = false
            Left = true
            Right = false
            if (!(controller.right.isPressed())) {
                Right = false
                direction_left = true
                direction_right = false
                animation.runImageAnimation(
                big_lizard,
                assets.animation`kaijuMomWalkLeft`,
                100,
                true
                )
            }
        } else {
            Idle = true
        }
    }
})
